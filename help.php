<h1>Помощь и поддержка</h1>
<p>Здесь вы найдете ответы на распространенные вопросы и инструкции по использованию сайта. Если не нашли ответ — напишите нам!</p>

<h2>Как начать?</h2>
<ul>
    <li>Зарегистрируйтесь на странице <a href="register.php">Регистрация</a>.</li>
    <li>Войдите в аккаунт на <a href="login.php">Вход</a>.</li>
    <li>Перейдите на главную и оставьте сообщение: выберите город, компанию, офис и тип (предложение или жалоба).</li>
    <li>Сообщения видны всем авторизованным пользователям.</li>
    <li>Выход — в правом верхнем углу.</li>
</ul>

<h2>Часто задаваемые вопросы (FAQ)</h2>
<dl>
    <dt>Могу ли я редактировать свое сообщение?</dt>
    <dd>Да, если вы автор. Нажмите "Редактировать" под сообщением.</dd>
    
    <dt>Как голосовать?</dt>
    <dd>Используйте кнопки "up" или "down" рядом с сообщением. Вы можете изменить голос.</dd>
    
    <dt>Что делать, если забыл пароль?</dt>
    <dd>Пока функция восстановления не реализована. Свяжитесь с поддержкой по email.</dd>
    
    <dt>Могу ли я удалить сообщение?</dt>
    <dd>Да, если вы автор или администратор. Ответы удалятся автоматически.</dd>
    
    <dt>Как фильтровать сообщения?</dt>
    <dd>На главной странице используйте селекты для города, компании, офиса и типа. Сортировка по голосам или дате.</dd>
</dl>

<h2>Советы по использованию</h2>
<ul>
    <li>Будьте вежливы в сообщениях — это помогает конструктивному диалогу.</li>
    <li>Добавляйте детали: фото, ссылки (если нужно расширить функционал).</li>
    <li>Если вы админ, используйте панель для управления справочниками и сообщениями.</li>
</ul>

<h2>Безопасность</h2>
<p>Мы используем хэширование паролей (BCRYPT) и защиту от SQL-инъекций. Не делитесь паролем. Если заметили проблему — сообщите.</p>

