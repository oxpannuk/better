<h1>О нас</h1>
<p>Мы — сайт приема предложений, созданный для улучшения работоспособности в офисах. Здесь вы можете зарегистрироваться, войти и оставлять сообщения с идеями или жалобами.</p>

<h2>Наша миссия</h2>
<p>Мы стремимся создать платформу, где сотрудники компаний могут делиться идеями по оптимизации рабочих процессов, повышению комфорта и решению проблем. Наш сервис помогает руководителям собирать обратную связь и внедрять улучшения, делая офисы лучше для всех.</p>

<h2>История создания</h2>
<p>Проект "better." был запущен в 2025 году как простая гостевая книга для офисов. С тех пор мы добавили авторизацию, голосование, фильтры по городам и компаниям, а также админ-панель. Мы продолжаем развиваться, чтобы стать незаменимым инструментом для корпоративной культуры.</p>

